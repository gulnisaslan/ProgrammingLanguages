#include <stdio.h>

int main()
{
    int i = 3;
    printf("%d\n",i);
    
    i += 5;  // i = i + 5
    printf("%d\n",i);

    i -= 5;  // i = i - 5
    printf("%d\n",i);

    i *= 3;  // i = i * 3
    printf("%d\n",i);

    i /= 3;  // i = i / 3
    printf("%d\n",i);
   

    return 0;
}
